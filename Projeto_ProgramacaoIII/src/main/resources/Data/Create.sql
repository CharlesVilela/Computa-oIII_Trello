CREATE table USUARIO (
	ID INT,
	USERNAME VARCHAR NOT NULL,
	EMAIL VARCHAR UNIQUE NOT NULL,
	PASSWORD VARCHAR NOT NULL,
	
	CONSTRAINT USUARIO_PK PRIMARY KEY(ID)
);

CREATE TABLE QUADRO (
	ID INT,
	NAME VARCHAR NOT NULL,
	ID_USUARIO INT NOT NULL,
	
	CONSTRAINT QUADRO_PK PRIMARY KEY(ID),
	CONSTRAINT ID_USUARIO_FK FOREIGN KEY(ID_USUARIO) REFERENCES USUARIO(ID) ON UPDATE CASCADE ON DELETE CASCADE
	
);

CREATE TABLE LISTA(
	ID INT,
	NAME VARCHAR NOT NULL,
	ID_QUADRO INT NOT NULL,
	
	CONSTRAINT LISTA_PK PRIMARY KEY(ID),
	CONSTRAINT ID_QUADRO_FK FOREIGN KEY(ID_QUADRO) REFERENCES QUADRO(ID) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE CARD(
	ID INT,
	NAME VARCHAR NOT NULL,
	DESCRIPTION VARCHAR,
	ID_LISTA INT,
	
	CONSTRAINT CARD_PK PRIMARY KEY(ID),
	CONSTRAINT ID_LISTA_FK FOREIGN KEY(ID_LISTA) REFERENCES LISTA(ID) ON UPDATE CASCADE ON DELETE CASCADE
);